    <ul id="softskills" class="two-columns ">
        <li><strong>Gestion de projets</strong> Coordination de multiples projets en parallèle avec respect des échéances strictes.</li>
        <li><strong>Communication efficace</strong> Collaboration avec les équipes et parties prenantes, offrant des retours constructifs et motivants.</li>
        <li><strong>Analyse critique</strong> Évaluation des processus et des performances, avec recommandations basées sur des données précises.</li>
        <li><strong>Proactivité</strong> Anticipation des problèmes potentiels et proposition de solutions avant qu'ils ne deviennent des obstacles.</li>
        <li><strong>Adaptabilité</strong> Flexibilité pour s’ajuster rapidement aux évolutions et aux nouvelles pratiques du secteur.</li>
        <li><strong>Rigueur méthodique</strong> Approche structurée et méthodique pour la gestion de projets.</li>
        <li><strong>Passion pour l’efficacité</strong> Engagement à améliorer continuellement la performance et l’efficacité, recherchant constamment des moyens d’optimiser les processus.</li>
        <li><strong>Intégrité et discrétion</strong> Respect strict des normes éthiques et souci particulier de la confidentialité.</li>

    </ul>
