        <p>Amateur des <b>beaux-arts</b> : peinture, sculpture, j'aime consacrer une partie de mon temps libre à
            l'exploration de l'art.</p>
        <p>Attiré par la <b>photographie et patrimoine</b>, j'aime réaliser des clichés soignés de paysages et en
            rapport avec les territoires et leur passé. Je suis activement investi dans l'association Il était une fois
            Rougiers, consacré à la promotion du patrimoine de mon village. Je suis également le rédacteur et le
            photographe (pour la grand majorité) du plus grand site Internet francophone sur la Bulgarie :
            https://la-bulgarie.fr</p>

        <p>Amateur de <b>philosophie</b>, j'aime allier l'epreuve de l'esprit à l'epreuve du coprs. Je pratique le <b>Kendo</b>,
            art d'escrime japonnais depuis 25 ans. J'ai atteint le 5e dan et fus une fois <b>manager de l'équipe de
                Bulgarie</b> aux championats du monde en 2013, et une fois aux championnats d'Europe en 2014.</p>

        <p><b>Appréciant le contact social</b>, j'aime les débats et discussions de qualité. A l'aise en public, j'ai pu
            présenter diverses organisations lors d'évènements publics comme salons internationaux ou de commerce. Lors
            de mon service militaire, j'étais sélectionné pour encadrer les nouveaux appelés. Je parle et écrit
            couramment l'anglais à des fins personnels et professionnels, ainsi que le bulgare, qui est ma langue
            native.</p>

        <p><b>Mon expérience dans le tourisme</b> en début de carrière m'a offert la possibilité de pouvoir travailler
            en cabinets de consultants spécialisés ainsi que pour des organisations non-gouvernementales menant des
            politiques de developpement territorial, en créant des synergies entres des acteurs des sphères public et
            privé. Je suis également le créateur et propriétaire d'une agence de voyage familiale - Patrimonia, centrée
            sur les voyages culturels en Bulgarie, partenaire exclusif sur cette destination pour Voyageurs du Monde, le
            plus prestigieux des tours-opérateurs français.</p>

        <p>Et ma passion pour la créativité qu'offre le monde de la <b>programmation web moderne</b> est la raison pour
            laquelle vous lisez sans doute cette page :) </p>
